package week3.day2;

public class ForLoopCase {
    public static void main(String[] args) {
        for (int i = 0; 2 * i < 18; i++) {
            System.out.println(i);
        }
        for (int i = 0; i * i < 50 ; i++) {
            System.out.println(i);
        }
        for (int i = 0; true; i++) {
            System.out.println(i);
        }
    }
}

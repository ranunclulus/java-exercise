package week2.day1;

public class DevideintoTwo {
    public static void main(String[] args) {
        float fOne = 1;
        float fTwo = 2;
        int iOne = 1;
        int iTwo = 2;
        System.out.printf("Float %f\n", fOne + fOne / fTwo);
        System.out.printf("Int %d\n", iOne + iOne / iTwo);
        System.out.printf("Float %f\n", 9 / 2.0);
        System.out.printf("Int %d\n", 9 / 2);
    }
}

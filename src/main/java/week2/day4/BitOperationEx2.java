package week2.day4;

public class BitOperationEx2 {
    public static void main(String[] args) {
        int iVal = 1;
        System.out.println(iVal);
        System.out.println(Integer.toBinaryString(iVal));
        System.out.println(~iVal);
        System.out.println(Integer.toBinaryString(~iVal));
    }
}

package week2.day4;

public class ComparisionOperatorsEx {
    public static void main(String[] args) {
        int iVal1 = 10;
        int iVal2 = 20;
        System.out.printf("iVal1 == iBal2 --> %b\n", iVal1 == iVal2);
        System.out.printf("iVal1 != iBal2 --> %b\n", iVal1 != iVal2);
        System.out.printf("iVal1 < iBal2 --> %b\n", iVal1 < iVal2);
        System.out.printf("iVal1 <= iBal2 --> %b\n", iVal1 <= iVal2);
        System.out.printf("iVal1 > iBal2 --> %b\n", iVal1 > iVal2);
        System.out.printf("iVal1 >= iBal2 --> %b\n", iVal1 >= iVal2);
    }
}
